package com.puce.kidtasks_ar;

public class Child {
    String id;
    String name;
    Integer streak;

    //id get-set
    public String getIdChild() {
        return id;
    }
    public void setIdChild(String id) {
        this.id = id;
    }

    //description get-set
    public String getNameChild() {
        return name;
    }
    public void setNameChild(String name) {
        this.name = name;
    }

    //completed get-set
    public Integer getStreakChild() {
        return streak;
    }
    public void setStreakChild(Integer streak) {
        this.streak = streak;
    }
}
